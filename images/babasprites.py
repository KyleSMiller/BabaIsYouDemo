left = box * 8
top = box * 36
right = box * 9
bottom = box * 37
counter = 0
basename = 'sprite_%d.png'

for j in range(3):
  sprite = sheet.crop((left, top, right, bottom))
  top = top + box
  bottom = bottom + box
  counter +=1
  name = basename % counter
  sprite.save(name)
  j += 1

fullSprite = Image.new('RGBA', (3*box, box))
for i in range(3):
  i += 1
  fullSprite.paste(Image.open('sprite_'+ str(i) + '.png'),(0 + ((i*box) - box), 0))
fullSprite.save('wordI.png', 'png')

