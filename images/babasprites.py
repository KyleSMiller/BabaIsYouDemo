#cuts out boxes top down first
#bunny right
box = 24 #pixels
left = 0
top = 0
right = 24
bottom = 24

counter = 0
basename = 'sprite_%d.png'

for i in range(5):
  for j in range(3):
    sprite = sheet.crop((left, top, right, bottom))
    top = top + box
    bottom = bottom + box
    counter +=1
    name = basename % counter
    sprite.save(name)
    j += 1
  top = 0
  bottom = box
  left = left + box
  right = right + box
  i+=1

#pastes sprite boxes together horizontally
fullSprite = Image.new('RGBA', (15*box, box))
for i in range(15):
  i += 1
  fullSprite.paste(Image.open('sprite_'+ str(i) + '.png'),(0 + ((i*box) - box), 0))
  
